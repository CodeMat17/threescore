// Strips _id and _creationTime from a document so Convex generates fresh ones
// on insert. Destructured as parameters so TypeScript's noUnusedParameters rule
// ignores the intentionally discarded _-prefixed bindings.
function stripSystem<T extends { _id: unknown; _creationTime: unknown }>(
  { _id, _creationTime, ...rest }: T
): Omit<T, "_id" | "_creationTime"> {
  void _id;
  void _creationTime;
  return rest as Omit<T, "_id" | "_creationTime">;
}

// makeFunctionReference avoids a circular dependency on the generated
// internal API object (which is re-generated by `npx convex dev`).
